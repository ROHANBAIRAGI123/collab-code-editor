import mongoose from "mongoose";

const userSchema = new mongoose.Schema({
  clerkUserId: { type: String, unique: true, required: true },
  username: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    enum: ["admin", "user"],
    default: "user",
  },
});

export const User = mongoose.model("User", userSchema);
